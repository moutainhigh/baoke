package com.baoke.admin.util;

public class Rules{
	public String getfield() {
		return field;
	}
	public void setfield(String field) {
		this.field = field;
	}
	public String getOp() {
		return op;
	}
	public void setOp(String op) {
		this.op = op;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	private String field;
	private String op;
	private String data;
}
